life_cnt = 5
letters_filled = 0
print(random_word)
arr = []
arr = ['_']*len(random_word)

while life_cnt>-1 and letters_filled<len(random_word):
    user_choice = input("Guess the letter : ").lower()
    if random_word.find(user_choice) == -1:
        print(f"You guessed {user_choice}, that's not in the word. You lose a life.")
        for i in arr:
            print(i,end=" ")
        print(stages[life_cnt])
        life_cnt -= 1
    else:
        for i in range(len(random_word)):
            if random_word[i] == user_choice:
                arr[i] = user_choice
                letters_filled += 1
        for i in arr:
            print(i,end=" ")
        print()
if life_cnt == -1:
    print("You Lose")
else:
    print(f"You Won,{str(arr)}")



