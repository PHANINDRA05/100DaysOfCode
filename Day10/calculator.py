def add(num1, num2):
    return num1 + num2
def sub(num1, num2):
    return num1 - num2
def mul(num1, num2):
    return num1 * num2
def div(num1, num2):
    return num1 / num2

def calculator():
    print(logo)
    num1 = float(input("What's the first number?: "))
    operations = {
        '+' : add,
        '-': sub,
        '*': mul,
        '/': div
    }
    for operator in operations:
        print(operator)

    while True:
        operator = input("Pick an operation: ")
        num2 = float(input("What's the next number?: "))
        calling_func = operations[operator]
        ans = calling_func(num1,num2)
        print(f"{num1} {operator} {num2} = {ans}")
        restart = input("Type 'y' to continue calculating with 11.0, or type 'n' to start a new calculation: ").lower()
        if restart == 'y':
            num1 = ans
        else:
            calculator()

calculator()
